# ─────────────────────────────────────────
# 1. append() / insert() — 요소 추가
# ─────────────────────────────────────────
# append(x)   : 리스트 맨 끝에 x 추가
# insert(i, x): 인덱스 i 위치에 x 삽입 (기존 요소는 오른쪽으로 밀림)

nums = [10, 20, 30]
nums.append(40)        # → [10, 20, 30, 40]
nums.insert(1, 15)     # → [10, 15, 20, 30, 40]  (인덱스 1에 15 삽입)
nums.append(20)        # → [10, 15, 20, 30, 40, 20]
print(nums)            # [10, 15, 20, 30, 40, 20]

print()

# ─────────────────────────────────────────
# 2. pop() — 요소 꺼내기 (제거 + 반환)
# ─────────────────────────────────────────
# pop()   : 마지막 요소를 꺼냄
# pop(i)  : 인덱스 i의 요소를 꺼냄
# ※ 꺼낸 값은 변수에 저장해 활용 가능

data = [1, 2, 3, 4, 5]
x = data.pop()         # 마지막 요소 5 꺼냄 → x = 5
y = data.pop(1)        # 인덱스 1의 요소 2 꺼냄 → y = 2
print(x, y)            # 5 2
print(data)            # [1, 3, 4]

print()

# ─────────────────────────────────────────
# 3. remove() — 값으로 요소 삭제
# ─────────────────────────────────────────
# remove(x) : x와 일치하는 첫 번째 요소만 제거
# ※ 같은 값이 여러 개 있어도 맨 앞의 것 하나만 삭제됨

items = ["a", "b", "c", "b", "d"]
items.remove("b")      # 첫 번째 "b"만 제거
print(items)           # ['a', 'c', 'b', 'd']

print()

# ─────────────────────────────────────────
# 4. sort() / reverse() — 정렬 / 뒤집기
# ─────────────────────────────────────────
# sort()    : 오름차순 정렬 (원본 변경)
# reverse() : 현재 순서를 반대로 뒤집음 (정렬 아님)

scores = [40, 10, 70, 30, 50]
scores.sort()          # → [10, 30, 40, 50, 70]  오름차순
scores.reverse()       # → [70, 50, 40, 30, 10]  뒤집기
print(scores)          # [70, 50, 40, 30, 10]

print()

# ─────────────────────────────────────────
# 5. 짝수 필터링 — append() + 조건문
# ─────────────────────────────────────────
# % 2 == 0 으로 짝수 판별 후 새 리스트에 추가

nums = [1, 2, 3, 4, 5, 6]
even = []
for n in nums:
    if n % 2 == 0:
        even.append(n)
print(even)            # [2, 4, 6]

print()

# ─────────────────────────────────────────
# 6. 인덱싱 / 슬라이싱 / len()
# ─────────────────────────────────────────
# list[i]    : 인덱스 i의 요소 (음수 인덱스: 뒤에서 셈)
# list[s:e]  : 인덱스 s 이상 e 미만 범위 추출
# len(list)  : 리스트 요소 개수 반환

letters = ["p", "y", "t", "h", "o", "n"]
print(letters[0], letters[-1])  # p n  (첫 번째, 마지막)
print(letters[1:4])             # ['y', 't', 'h']  (인덱스 1~3)
print(len(letters))             # 6

print()

# ─────────────────────────────────────────
# 7. 합계 / 평균 계산
# ─────────────────────────────────────────
# for 루프로 누적 합산 후 길이로 나눠 평균 계산

data = [10, 20, 30, 40]
total = 0
for x in data:
    total += x                  # 10 → 30 → 60 → 100
avg = total / len(data)         # 100 / 4 = 25.0
print(total, avg)               # 100 25.0

print()

# ─────────────────────────────────────────
# 8. insert() / remove() / append() 조합
# ─────────────────────────────────────────

nums = [1, 3, 5, 7]
nums.insert(2, 4)      # 인덱스 2에 4 삽입 → [1, 3, 4, 5, 7]
nums.remove(7)         # 7 제거            → [1, 3, 4, 5]
nums.append(6)         # 끝에 6 추가       → [1, 3, 4, 5, 6]
print(nums)            # [1, 3, 4, 5, 6]

print()

# ─────────────────────────────────────────
# 9. 중복 제거 — not in 활용
# ─────────────────────────────────────────
# v not in unique : unique 리스트에 없는 값만 추가해 중복 제거

values = [1, 2, 2, 3, 3, 3, 4]
unique = []
for v in values:
    if v not in unique:         # 아직 없는 값만
        unique.append(v)
print(unique)                   # [1, 2, 3, 4]

# ─────────────────────────────────────────
# 10. 2차원 리스트(행렬) 전체 합산
# ─────────────────────────────────────────
# 이중 for 루프로 행(row) → 각 요소(x) 순서로 순회

matrix = [[1, 2, 3],
          [4, 5, 6]]
total = 0
for row in matrix:              # row = [1,2,3] → [4,5,6]
    for x in row:               # x = 각 숫자
        total += x
print(total)                    # 21  (1+2+3+4+5+6)

print()

# ─────────────────────────────────────────
# 11. 복합 연산 추적 (pop / reverse / insert)
# ─────────────────────────────────────────
# 단계별로 따라가야 결과를 알 수 있는 고난도 문제

nums = [4, 1, 9, 2, 8, 3]
a = nums.pop(2)        # 인덱스 2의 9 꺼냄 → a=9, nums=[4,1,2,8,3]
nums.reverse()         # → [3, 8, 2, 1, 4]
nums.insert(1, a * 3)  # 인덱스 1에 27 삽입 → [3, 27, 8, 2, 1, 4]
b = nums.pop()         # 마지막 4 꺼냄 → b=4, nums=[3, 27, 8, 2, 1]
nums.insert(nums[2], b // 2)
# nums[2]=8, b//2=2 → 인덱스 8에 2 삽입
# 리스트 길이가 5라서 인덱스 8은 맨 뒤에 추가됨
# → [3, 27, 8, 2, 1, 2]
print(nums)            # [3, 27, 8, 2, 1, 2]

print()

# ─────────────────────────────────────────
# 12. 슬라이싱 심화 — step(간격) 활용
# ─────────────────────────────────────────
# list[start:stop:step]
#   step 양수 : start → stop 방향으로 step 간격 추출
#   step 음수 : start → stop 방향으로 거꾸로 추출 (stop 미포함)
# ※ step 방향과 start→stop 방향이 반대면 빈 리스트 반환

lst = [10, 20, 30, 40, 50, 60]

slice1 = lst[4:1:-1]   # 인덱스 4(50)에서 인덱스 1 직전까지 -1 간격 (역방향)
# → [50, 40, 30]
slice2 = lst[4:1:1]    # 인덱스 4에서 인덱스 1 방향으로 +1 간격인데,
# start > stop 이므로 방향 불일치 → 빈 리스트
# → []
slice3 = lst[-2:0:-2]  # 인덱스 -2(50)에서 인덱스 0 직전까지 -2 간격 (역방향)
# 50(idx4) → 30(idx2) → 멈춤  (인덱스 0은 미포함)
# → [50, 30]
print(slice1)          # [50, 40, 30]
print(slice2)          # []
print(slice3)          # [50, 30]